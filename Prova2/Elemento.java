class Elemento {
	private int altura, largura;
	private double angulacao;
	
	public int getAltura() {
		return this.altura;
	}
	public int getLargura() {
		return this.largura;
	}
	public void setAltura(int a) {
		this.altura = a;
	}
	public void setLargura(int l) {
		this.largura = l;
	}
	public double getAngulacao() {
		return this.angulacao;
	}
	public void setAngulacao(double d) {
		this.angulacao = d;
	}
}
